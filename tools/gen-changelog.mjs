#!/usr/bin/env node
/**
 * gen-changelog.mjs — Multi-team changelog generator
 *
 * Queries the tasks API for recently completed work and formats it as a
 * human-readable changelog digest. Works against any reflectt-node instance.
 *
 * Usage:
 *   node tools/gen-changelog.mjs
 *   node tools/gen-changelog.mjs --base http://localhost:4445 --days 7 --format markdown
 *
 * Options:
 *   --base <url>       API base URL (default: http://localhost:4445)
 *   --days <n>         Look back N days (default: 7)
 *   --format markdown  Output format: markdown | text (default: markdown)
 *   --team <name>      Filter to a specific team/assignee
 *   --min-priority P1  Only include tasks at or above this priority
 */

import { parseArgs } from 'node:util';

const { values } = parseArgs({
  options: {
    base:         { type: 'string',  default: 'http://localhost:4445' },
    days:         { type: 'string',  default: '7' },
    format:       { type: 'string',  default: 'markdown' },
    team:         { type: 'string',  default: '' },
    'min-priority': { type: 'string', default: '' },
  },
  strict: false,
});

const BASE     = values.base;
const DAYS     = parseInt(values.days, 10);
const FORMAT   = values.format;
const TEAM     = values.team;
const MIN_PRI  = values['min-priority'];

const PRIORITY_ORDER = ['P0', 'P1', 'P2', 'P3'];

function meetsMinPriority(taskPriority, minPriority) {
  if (!minPriority) return true;
  const ti = PRIORITY_ORDER.indexOf(taskPriority);
  const mi = PRIORITY_ORDER.indexOf(minPriority);
  if (ti === -1 || mi === -1) return true;
  return ti <= mi;
}

async function fetchDoneTasks() {
  const cutoff = new Date(Date.now() - DAYS * 24 * 60 * 60 * 1000).toISOString();
  const params = new URLSearchParams({ status: 'done', limit: '200' });
  if (TEAM) params.set('assignee', TEAM);

  const res = await fetch(`${BASE}/tasks?${params}`);
  if (!res.ok) throw new Error(`API error ${res.status}: ${await res.text()}`);
  const data = await res.json();
  const tasks = data.tasks || [];

  return tasks.filter(t => {
    const updatedAt = t.updatedAt || t.updated_at || t.createdAt || '';
    if (updatedAt && updatedAt < cutoff) return false;
    if (!meetsMinPriority(t.priority, MIN_PRI)) return false;
    return true;
  });
}

function groupByTeam(tasks) {
  const groups = {};
  for (const t of tasks) {
    const key = t.assignee || 'unassigned';
    if (!groups[key]) groups[key] = [];
    groups[key].push(t);
  }
  return groups;
}

function priorityLabel(p) {
  return p ? `[${p}]` : '';
}

function artifactLinks(t) {
  const arts = t.metadata?.artifacts || [];
  return arts.length ? arts.map(a => `[→ ${a.replace('https://github.com/', '')}](${a})`).join(', ') : '';
}

function formatMarkdown(tasks) {
  const now = new Date().toISOString().slice(0, 10);
  const lines = [
    `# Team Changelog — ${now}`,
    `> Generated from tasks completed in the last ${DAYS} day${DAYS !== 1 ? 's' : ''}`,
    `> Source: \`${BASE}/tasks?status=done\``,
    '',
  ];

  if (tasks.length === 0) {
    lines.push('_No completed tasks in this window._');
    return lines.join('\n');
  }

  const groups = groupByTeam(tasks);
  const agents = Object.keys(groups).sort();

  for (const agent of agents) {
    lines.push(`## @${agent}`);
    lines.push('');
    const agentTasks = groups[agent].sort((a, b) => {
      const ai = PRIORITY_ORDER.indexOf(a.priority);
      const bi = PRIORITY_ORDER.indexOf(b.priority);
      return (ai === -1 ? 99 : ai) - (bi === -1 ? 99 : bi);
    });
    for (const t of agentTasks) {
      const pri = priorityLabel(t.priority);
      const links = artifactLinks(t);
      const line = `- ${pri ? pri + ' ' : ''}**${t.title}**${links ? '  ' + links : ''}`;
      lines.push(line);
    }
    lines.push('');
  }

  lines.push('---');
  lines.push(`_${tasks.length} task${tasks.length !== 1 ? 's' : ''} shipped. Generated by tools/gen-changelog.mjs_`);
  return lines.join('\n');
}

function formatText(tasks) {
  const now = new Date().toISOString().slice(0, 10);
  const lines = [
    `TEAM CHANGELOG — ${now}`,
    `Last ${DAYS} day${DAYS !== 1 ? 's' : ''} · ${BASE}`,
    '='.repeat(60),
    '',
  ];

  if (tasks.length === 0) {
    lines.push('No completed tasks in this window.');
    return lines.join('\n');
  }

  const groups = groupByTeam(tasks);
  for (const [agent, agentTasks] of Object.entries(groups).sort()) {
    lines.push(`@${agent}`);
    for (const t of agentTasks) {
      const pri = priorityLabel(t.priority);
      lines.push(`  ${pri ? pri + ' ' : ''}${t.title}`);
    }
    lines.push('');
  }

  lines.push(`${tasks.length} task${tasks.length !== 1 ? 's' : ''} shipped.`);
  return lines.join('\n');
}

try {
  const tasks = await fetchDoneTasks();
  const output = FORMAT === 'text' ? formatText(tasks) : formatMarkdown(tasks);
  process.stdout.write(output + '\n');
} catch (err) {
  console.error('gen-changelog error:', err.message);
  process.exit(1);
}
